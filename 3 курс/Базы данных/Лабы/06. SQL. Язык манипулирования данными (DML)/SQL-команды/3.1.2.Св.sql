SELECT Блюдо, В, Основа, Выход, Труд
    FROM Блюда, Состав, Продукты
    WHERE Блюда.БЛ = Состав.БЛ 
    AND Состав.ПР = Продукты.ПР 
    AND Продукты.Продукт = 'Майонез' 
    AND Выход = (
    SELECT MIN(Выход)
        FROM Блюда, Состав, Продукты
        WHERE Блюда.БЛ = Состав.БЛ 
        AND Состав.ПР = Продукты.ПР 
        AND Продукты.Продукт = 'Майонез'
    );